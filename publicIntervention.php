			the Alice in Wonderland Statue, the Flatiron Building, Grand Central Station and Washington Square Park. 
			</p>
			
			<img src="images/interaction/isfs/charts.png">
			
			<p>
			Based on the type of people, the traffic, the congestion and the pace of each location we decided to focus on two locations: the Flatiron Building and Washington Square Park.  
			</p>
			
			<p>					
			To implement the project, we created three things: a frame mounted on a tripod, a twitter hashtag and a sign to graphically explain to passers by to take a picture and tweet it. 
